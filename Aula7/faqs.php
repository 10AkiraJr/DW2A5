    <h2>1 - O que é HTML?</h2>
    <P>É uma linguagem de computador que compõe a maior parte das páginas da internet e dos aplicativos online. 
        Um hipertexto é um texto usado para fazer referência a outros textos, enquanto uma linguagem de marcação 
        é composta por uma série de marcações que dizem para os servidores da web qual é o estilo e a estrutura 
        de um documento.
    </P>

    <h2>2 - HTML é uma linguem de programação?</h2>
    <P>
        O HTML não é considerado uma linguagem de programação, já que ele não pode criar funcionalidades dinâmicas. 
        Ao invés disso, com o HTML, os usuários podem criar e estruturar seções, parágrafos e links usando elementos, 
        tags e atributos.
    </P>

    <h2>3 - Qual o uso mais comum do HTML?</h2>
    <P>
        Desenvolvimento web: Os desenvolvedores usam códigos HTML para projetar como um navegador vai exibir os 
        elementos das páginas, como textos, hiperlinks e arquivos de mídia.
    </P>
    <P>
        Navegação na internet: Os usuários podem navegar facilmente e inserir links entre páginas e sites relacionados, 
        já que o HTML é amplamente usado para incorporar hiperlinks.
    </P>
    <P>
        Documentação: O HTML torna possível a organização e a formatação de documentos, de maneira similar ao Microsoft Word.
